package org.ginafro.notenoughfakepixel.features.capes;

public class Cape {

    public int width, height;
    public int capeID;
    public String capeFile, capeName;

    public Cape(int w, int h, int id, String file, String name) {
        width = w;
        height = h;
        capeID = id;
        capeFile = file;
        capeName = name;
    }

}
